        {
            IniciarTeste(TipoArquivo.ParcEmissao, "4062", "FG02 - PROC26 - Informar no campo CD_PRODUTO valor diferente do parametrizado na tabela TAB_PRM_PRODUTO_7003");
            arquivo = new Arquivo_Layout_9_4_ParcEmissao();
